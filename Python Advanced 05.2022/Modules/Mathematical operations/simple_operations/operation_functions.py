def sum_nums(a, b):
    result = a + b
    return result


def subs_nums(a, b):
    result = a - b
    return result


def multiply_nums(a, b):
    result = a * b
    return result


def div_nums(a, b):
    result = a / b
    return result


def pow_nums(a, b):
    result = a ** b
    return result
